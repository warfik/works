package com.telran;

public class CreditUser {
    CreditUser() {
    }

    CreditUser(String nameIn, String surnameIn, int ageIn, int annualSalaryIn) {
        name = nameIn;
        surname = surnameIn;
        age = ageIn;
        annualSalary = annualSalaryIn;

    }

    String name;
    String surname;
    int age;
    int annualSalary;

}


