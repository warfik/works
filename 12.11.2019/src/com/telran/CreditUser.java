package com.telran;

public class CreditUser {
    CreditUser() {
    }

    CreditUser(String name, String surname, int age, int annualSalary) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.annualSalary = annualSalary;

    }

    String name;
    String surname;
    int age;
    int annualSalary;

}


